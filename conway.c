static char spielfeld[XMAX][YMAX];
static char temp[XMAX][YMAX];

int main(void)
{
  clock_t       t;
  unsigned long sec;
  unsigned      sec10;
  unsigned long fps;
  unsigned      fps10;
  unsigned char background;
  unsigned char text;
        
	short x;
	unsigned char y;
	unsigned char round = 0;

  t = clock ();
	initSpielfeld(spielfeld);
  clrscr();
	background = bgcolor(COLOR_BLACK);
	text = textcolor(COLOR_WHITE);
	printSpielfeld(spielfeld);
//	signal (int sig, __sigfunc func);


	while(round < ROUNDS && !kbhit()){
		for(y = 0; y< YMAX; y++){
			for(x = 0; x< XMAX; x++){
				findNachbarn(x,y,spielfeld, temp);
			}// for x
		}// for y

		memcpy(spielfeld,temp,XMAX*YMAX);
	
		round++;
		printSpielfeld(spielfeld);	
	}
		t = clock() - t;
	
  /* Reset screen colors */
    bgcolor (background);
    textcolor (text);
    clrscr ();

    /* Calculate stats */
    sec   = (t * 10) / CLK_TCK;
    sec10 = sec % 10;
    sec  /= 10;
    fps   = (round * (CLK_TCK * 10)) / t;
    fps10 = fps % 10;
    fps  /= 10;

    /* Output stats */
    gotoxy (0, 0); cprintf ("time  : %lu.%us", sec, sec10);
    gotoxy (0, 1); cprintf ("frames: %lu", round);
    gotoxy (0, 2); cprintf ("fps   : %lu.%u", fps, fps10);

    /* Wait for a key, then end */
    cputsxy (0, 4, "Press any key when done...");
    (void) cgetc ();

    /* Done */
    return EXIT_SUCCESS;
}





void findNachbarn(short x, unsigned char y, char spielfeld[][YMAX], char temp[][YMAX]){
	//gehe Ã¼ber alle nachbarn
	unsigned char osx;
	unsigned char osy; 
	unsigned char ofy;
	unsigned char ofx;

	// p(x/y) ist ein Punkt
	
	char lebende = 0;



	for(ofy = y-1; ofy <= (unsigned char)y+1; ++ofy){
		for(ofx = x-1; ofx <= (unsigned char)x+1; ++ofx){
	
			if(ofy > YMAX-1)	{
				if(ofy == 255){
					osy = YMAX-1;		//Wenn p in der ersten Zeile osy -> letzten Zeile
				}else{
					osy = 0;
				}
			}
				else {
					osy = ofy;		//Weder p in der ersten noch in der letzten: osy -> ofy
				}
			
			
			if( ofx < 0)	{
				osx = XMAX-1;
			} else if( ofx > XMAX-1)	{
					osx = 0;
				}
				else {
					osx = ofx;
				}
			lebende += spielfeld[osx][osy];				
		}//for ofx
	}//for ofy	

	lebende -= spielfeld[x][y];

	//Geht besser
	if(spielfeld[x][y] == 0 ){
		if(lebende == 3){
			temp[x][y] = 1;
//			printf("t3\n\n");
		}
	}else{
		if(lebende < 2 || lebende > 3){
			temp[x][y] = 0;
		}else{
			temp[x][y] = 1;
		}
	}
}




